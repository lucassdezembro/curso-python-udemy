# Tipo de dado bool (boolean)
# São as repostas possíveis para qualquer condição:
# True ou False
# Utilizando o ==, podemos verificar se um valor é igual a outro, e sua resposta será um booleano

print("10 é igual a 10?", 10 == 10)
print("10 é igual a 11?", 10 == 11)
print("10 é igual a 11?", 10 == 11, type(10 == 11))