"""

Operadores de comparação (operadores relacionais)

Operador            Significado         Exemplo (True)
>                   maior               2 > 1
>=                  maior ou igual      2 >= 2
<                   menor               2 < 3
<=                  menor ou igual      2 <= 2
==                  igual               'a' == 'a'
!=                  diferente           'a' != 'b'

"""

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print('2 é maior que 1?', maior)
print('2 é maior ou igual a 2?', maior_ou_igual)
print('1 é menor do que 2?', menor)
print('2 é menor ou igual a 2?', menor_ou_igual)
print('"a" é igual a "a"?', igual)
print('"a" é diferente de "b"?', diferente)