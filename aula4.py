# Tipos int
# Números inteiros
print(11)
print(-11)
print(0, end="\n\n")


# Tipo float
# Números com ponto flutuante
print(12.5)
print(-12.5)
print(0.0, end="\n\n")

# A função type() nos retorna o valor do dado
print("isso é uma string: ", type(""))
print("isso é um inteiro: ", type(0))
print("isso é um float: ", type(0.67))